#!/usr/bin/env node
import { build } from "esbuild";
import fs from "fs/promises";
import path from "path";
import { createRequire } from "module";

const ROOT = process.cwd();
const OUTPUT_FILE = path.join(ROOT, "runtime", "vendor", "opencode-sdk-v2-client.cjs");

async function main() {
  await fs.mkdir(path.dirname(OUTPUT_FILE), { recursive: true });

  await build({
    stdin: {
      contents: `
        import { createOpencodeClient } from "@opencode-ai/sdk/v2/client";
        export { createOpencodeClient };
      `,
      resolveDir: ROOT,
      sourcefile: "sdk-v2-client-entry.mjs",
      loader: "js",
    },
    bundle: true,
    format: "cjs",
    platform: "node",
    target: "es2020",
    outfile: OUTPUT_FILE,
    sourcemap: false,
    logLevel: "info",
    legalComments: "none",
    banner: {
      js: "/* Auto-generated by scripts/build-sdk-vendor.mjs. Do not edit manually. */",
    },
  });

  const require = createRequire(import.meta.url);
  const mod = require(OUTPUT_FILE);
  if (!mod || typeof mod.createOpencodeClient !== "function") {
    throw new Error("Vendor SDK bundle validation failed: createOpencodeClient is missing");
  }

  console.log(`[build-sdk-vendor] OK ${OUTPUT_FILE}`);
}

main().catch((error) => {
  const message = error instanceof Error ? error.message : String(error);
  console.error(`[build-sdk-vendor] FAILED: ${message}`);
  process.exitCode = 1;
});
